//週の労働が４０時間を超える場合の、”法定外残業時間数”の計算、修正
void weekTotal(void){
	if(sum_w > 40*60){			
		if((h1[i] == 8) && (m1[i] == 0) && total[i]>=480){			//日の労働が８時間を超える場合
			time1 = time1 - 60;	
		}else if((h1[i] == 8) && (m1[i] == 0) && total[i] > 420 && total[i] < 480){  //日の労働が８時間未満の場合
			time1 = time1 - (total[i] - 420);
		}else if(h1[i] != 8 || m1[i] != 0){								//不就労働の場合
			time1 = time1 - (480 - (420 - ((h2[i]*60 + m2[i]) - (h1[i]*60 + m1[i])))) ;	
		}
		//法定外残業時間
		if(total[i] > 480 && sum_w < 40*60){
			time2 = time2 - (total[i] - 480);
		}
				
	time2 = time2 + sum_w - 40*60 - (total[i] - 480);
		
	if(week == 5) sum_w = 0;
	}
}

