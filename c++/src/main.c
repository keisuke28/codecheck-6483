//日付から曜日を判定する
//返し値 {0:日曜日 1:月曜日 2:火曜日 3:水曜日 4:木曜日 5:金曜日 6:土曜日}
int getWeekday(int y, int m, int d){		//引数 {年、月、日}
	 if( m < 3 ) {
        y--; m += 12;
    }
    return ( y + y/4 - y/100 + y/400 + ( 13*m + 8 )/5 + d )%7;
}

/*************************************************************************************************/
//分→時の変換（３０分未満切り捨て、３０分以上繰り上げ）
int convertTime(int t){		//引数：分表記の時間					
	if(t%60 < 30){
		t = t / 60;
	}else{
		t = t / 60 +1;
	}
	return t;		//返し値：時表記の時間
}
